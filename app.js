app = {
  sum: (a, b) => {
    return a + b;
  },
  sub: (a, b) => {
    return a - b;
  },
  mul: (a, b) => {
    return a * b;
  },
  div: (a, b) => {
    return a / b;
  },
  pow: (a, b) => {
    return Math.pow(a, b);
  },
  sqrt: (a) => {
    return Math.sqrt(a);
  },
};

module.exports = app;
